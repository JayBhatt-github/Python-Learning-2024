# Type casting in python 

a = "1"     #This is string
b = "2"     #This is string


# Explicit Typecasting - from user 
print(int(a) + int(b)) #Converted both the variables to integer with the help of type casting


# Implicit TypeCasting - from python interpreter (Automatic)
c = 1.9     #This is float
d = 8       #This is integer

print(c + d) #This will be float 