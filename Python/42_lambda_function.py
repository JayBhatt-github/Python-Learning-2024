# lambda function in python 

# A lambda function to add two numbers
add = lambda x, y: x + y
print("The sum is:", add(3, 5))

# A lambda function to multiply two numbers
multiply = lambda x, y: x * y
print("The product is:", multiply(3, 5))

# A lambda function to square a number
square = lambda x: x ** 2
print("The square is:", square(3))
