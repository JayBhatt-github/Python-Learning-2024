# Loops in python

# for loop
fruits = ["apple", "banana", "cherry"]  # Define a list of fruits
for x in fruits:  # Iterate over each item in the list
  print(x)  # Print the current item


# while loop
i = 0  # Initialize a counter variable
while i < 5:  # Continue the loop as long as the counter is less than 5
  print(i)  # Print the current value of the counter
  i = i+1  # Increment the counter by 1
