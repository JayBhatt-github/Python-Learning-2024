# Function in python 

# user-defined function 
def addition(x,y):      # declaring (define) the user defined function
    print("Addition of",x,"and",y,"is:",x+y)

a = int(input("Enter value of A:"))
b = int(input("Enter value of B:"))
addition(a,b)       # Calling the user defined function


# pre-defined function

# max function
print(max(a,b),"is max")        # it will return maximum number from 2 numbers

# min function
print(min(a,b),"is min")        # it will return minimum number from 2 numbers