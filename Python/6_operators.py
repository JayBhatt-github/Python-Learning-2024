# Simple python calculator for operation of two variables

a = 10
b = 6

print("the value of a + b is", a+b)      #addition
print("the value of a - b is", a-b)      #subtraction
print("the value of a * b is", a*b)      #multiplication
print("the value of a ^ b is", a**b)     #power a^b
print("the value of a / b is", a/b)      #division
print("the value of a % b is", a%b)      #mod (Modulus)
print("the value of a // b is", a//b)     #floor division (2.35 --> 2)