
# if statement
a = 33
b = 50

if (b > a):
  print("b is greater than a")   # indentation needed for executing conditional statements



# elif (else if) statement 
a1 = 33
b1 = 33

if (b1 > a1):
  print("b is greater than a")
elif b1 < a1:
  print("b is less than a")
elif a1 == b1:
  print("a and b are equal")



# if else statement 
a2 = 200
b2 = 33

if (b2 > a2):
  print("b is greater than a")
elif (a2 == b2):
  print("a and b are equal")
else:
  print("a is greater than b")



